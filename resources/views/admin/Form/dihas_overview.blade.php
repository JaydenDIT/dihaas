                <div>
                    <b>1.Principle of Die-in -harness Scheme:</b>
                    The objective of the scheme is to grant appointment on
                    compassionate grounds to the next of kin (a dependent family member) of a Government servant
                    who dies in harness leaving his family in penury and without any means of livelihood. The Scheme is
                    not extendable to generations and should not be considered as hereditary right. In other words, the
                    scheme shall not be admissible/extendable to dependent family member of a Government servant if
                    the Government Servant was appointed under Die-in -harness Scheme (DIH).




                </div>

<br>
                <!-- 2 -->

                <div>
                    <b>2. To whom applicable: </b>
                    <ol type="a">
                        <li>
                            The Scheme shall be applicable to unemployed dependent family member
                            of the deceased Government servant in the following order of entitlement:

                            <ol type="i">
                                <li>legal spouse</li>
                                <li> elder child (unmarried son/daughter or married son of the deceased Government
                                    Servant
                                    if living in the same household)</li>
                                <li>brother/ sister in case of unmaarried Government servant</li>
                            </ol>
                        </li>

                        <li>
                            No Objection Certificate i.e., NOC (in the form of Affidavit) from other eligible dependents
                            to
                            be submitted if the claimant is other than legal spouse.
                        </li>

                        <li>
                            Undertaking (in the form of Affidavit) to be submitted to look after the other dependent
                            family members if appointed and liable to be terminated from the post failing to comply the
                            scheme.
                        </li>

                        <li>
                            The scheme shall be applicable only to the dependent of such deceased Government servant who
                            was appointed on regular basis/post and not under DIH Scheme.
                        </li>
                        <li>
                            The scheme shall not cover those who work on daily wages, casual, apprentice,adhoc,
                            contract,
                            work-charged, muster roll, extension, re-employed basis and employees who retire on medical
                            ground.
                        </li>
                        <li>
                            The Scheme shall not cover missing/absconding Government servants and death caused by
                            suicide
                            or by excessive drinking or on account of natural death.
                        </li>
                        <li>
                            The Scheme shall not be applicable to adopted son/ adopted daughter.
                        </li>
                        <li>
                            The Government Servant should have put in at least 5 years regular service in the
                            Government.
                        </li>
                        <li>
                            Where the family of the deceased employee is in receipt of pension under the Manipur
                            Services
                            (Special Pension) Rules, 1983 or the Manipur Services (Liberalised Pension) Rules, 2000, the
                            claim for
                            appointment under the scheme shall not be made and the same shall not be entertained by the
                            Department concerned.
                        </li>
                    </ol>


                </div>


