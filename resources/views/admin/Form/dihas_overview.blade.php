                <div>

                    <b>3. Preference to be given under the Scheme:</b>

                    <ol type="a">
                        <li>Benefit under the Scheme will be available to
                            dependents of those Government servants who died while performing official duties viz.
                            election
                            /census /survey /research / official tour/ field inspection etc. or who died in insurgency
                            related
                            violence while performing official duties. These cases shall be categorised as
                            "Preference-I"
                            <br>
                            <b>Note:</b>: The proposal shall be subject to authentication with the Police Report of the
                            incident, Death
                            Certificate and other supporting documents on eligibility including
                            Unemployment Certificate issued by concerned DC/SDO.
                        </li>


                        <li>
                            After completing pending cases under Preference-I category, the next preference will be
                            given to
                            dependents of those Government servants who died not on duty but due to accidents (viz.,
                            motor
                            vehicle/ drowning/ fire/ natural calamities etc.) or unnatural death (excluding excessive
                            drinking and
                            suicide cases) leaving his/her family in the state of financial destitution, penury, and
                            without
                            any
                            means of livelihood, subject to availability of vacancy reserved for this scheme. This case
                            shall be
                            categorised as "Preference-II'

                            <br>
                            <b>Note:</b>The proposal is subject to authentication with the "Unemployment Certificate" of
                            the
                            family
                            members of the deceased Government Servant from the office of the concerned Deputy
                            Commissioner/Sub Divisional Officer. In other words, the DC/SDO has to affirm that none of
                            the
                            family member is working in Government sector and the family is in a state of financial
                            destitution,
                            penury and without any means of livelihood.
                        </li>
                    </ol>

