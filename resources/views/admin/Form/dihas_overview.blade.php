                <b>18.Procedure for appointment:</b>
                <br>
                Step.1: If the bereaved family is eligible for Die-in-harness scheme,
                the claimant shall submit the application to the Government (District level/Directorate level) within 6
                months of death. The officer who is looking after the welfare of the employees in the District level/
                Directorate level shall assist and advise the claimant about the requirements and formalities to be
                completed by him/her.
                <br><br>
                Step. 2: The District level office has to forward the application to Directorate level within 1 (one)
                month (in case of application submitted at district level office)
                <br><br>
                Step. 3: The Directorate level has to upload the application complete in all respects along with all
                necessary documents on the Central Portal without delay.
                <br> <br>
                Step. 4: The Nodal officer/official who handles the scheme in the Administrative Department shall
                check and verify the uploaded data on regular basis.
                <br><br>
                Step. 5: The Departmental Screening Committee of each Department shall screen and scrutinise the
                uploaded applications of the respective department on quarterly basis.
                <br>
                Original applications along with all necessary documents have to be placed before the Departmental
                Screening Committee by the Head of Department concerned for examination. Incomplete
                applications shall be rejected by the Committee.
                <br> <br>
                Step. 6: Administrative approval of the concerned department shall be obtained.
                <br> <br>
                Step.7: The Administrative Department shall forward all the screened proposals to the Department of
                Personnel online through the Central Portal only.
                <br> <br>
                Step.8: The administrator of the central registry (Department of Personnel) shall prepare the
                combined/inter-department vacancies and seniority list of applications in respect of all the
                departments. Combined Seniority Lists of all Departments as well as department wise Seniority List
                shall be generated by the Central Portal.
                <br> <br>
                Step. 9: The combined proposal for appointment under Die-in-harness based on the total vacancies
                available in all Departments and the combined Seniority List of all Departments as generated by the
                <b>Central Portal will be examined by an Inter-Departmental Screening Committee as defined at Para
                    No.15 above.</b>
                The Committee shall meet once every quarter and the recommendations of the
                Committee will be put up for concurrence in the Department of Personnel.
                <br> <br>
                Step.10: The decision of the competent authority in this regard will be conveyed to the respective
                department. If the proposal is/are concurred in the Department of Personnel, the concurrence will be
                conveyed in the form of U.O No, against the name of the claimant. The U.O. will be generated by the
                Central Portal.
                <br> <br>
                Step.11: Accordingly, the Administrative Department shall convey the approval of the Government to
                the Directorate level along with the U.O Nos. for issuance of Appointment Order promptly.
